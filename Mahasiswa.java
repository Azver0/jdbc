package jtbcTrain;

public class Mahasiswa {
	private int nim;
	private String nama;
	private String kelas;
	public int getNim() {
		return nim;
	}
	public void setNim(int nim) {
		this.nim = nim;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getKelas() {
		return kelas;
	}
	public void setKelas(String kelas) {
		this.kelas = kelas;
	}
	
	
}
